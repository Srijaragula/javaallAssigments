package allassignments;
 
public class CharacterComparision {

	public static void main(String[] args) {  
		char ch1='2';
		char ch2='5';
		System.out.println("Character one is:"+ch1);
		System.out.println("Character two is:"+ch2);
		
		if(ch1==ch2) {
			System.out.println("Both characters are same");
		}
		
		if(ch1>ch2) {
			System.out.println("ch1 is greater");
		} 
		else {
			System.out.println("ch2 is greater");
		}
	}
	

}
